import math

x = -2.5

# print the absolute value of x which is 2.5
print(abs(x))
# print the tuple containing quotient and remainder of 10 divided by 3, which is (3, 1)
print(divmod(10, 3))
# print the float representation of the string "3.14" which is 3.14
print(float("3.14"))
# print the hexadecimal representation of the integer 255 which is 0xff
print(hex(255))
# print the integer representation of the string "123" which is 123
print(int("123"))
# print the maximum number among 1, 2 and 3, which is 3
print(max(1, 2, 3))
# print the minimum number among 1, 2 and 3, which is 1
print(min(1, 2, 3))
# print the octal representation of the integer 8 which is 0o10
print(oct(8))
# print the result of 2 to the power of 3 which is 8
print(pow(2, 3))
# print the rounded value of 3.14 which is 3
print(round(3.14))

# math module functions
print(math.acos(-1))  # print the arc cosine of -1 which is 3.141592653589793
print(math.asin(0))  # print the arc sine of 0 which is 0.0
print(math.atan(1))  # print the arc tangent of 1 which is 0.7853981633974483
# print the arc tangent of y/x, here y = 1 and x = 1, which is 0.7853981633974483
print(math.atan2(1, 1))
print(math.ceil(3.14))  # print the smallest integer not less than 3.14 which is 4
print(math.cos(math.pi))  # print the cosine of pi which is -1.0
print(math.cosh(0))  # print the hyperbolic cosine of 0 which is 1.0
print(math.e)  # print the value of e which is 2.718281828459045
# print the value of e raised to the power of 1 which is 2.718281828459045
print(math.exp(1))
print(math.fabs(-2.5))  # print the absolute value of -2.5 which is 2.5
# print the largest integer not greater than 3.14 which is 3
print(math.floor(3.14))
print(math.fmod(10, 3))  # print the remainder of 10 divided by 3 which is 1.0
# print the mantissa and exponent of the number 2 which is (0.5, 2)
print(math.frexp(2))
# print the Euclidean norm of the vector (3,4) which is 5.0
print(math.hypot(3, 4))
# print the number 2 multiplied by 2 raised to the power of 3 which is 16
print(math.ldexp(2, 3))
print(math.log(math.e))  # print the natural logarithm of e which is 1.0
print(math.log10(100))  # print the base-10 logarithm of 100 which is 2.0
# print the fractional and integer parts of 3.14 which is (0.14000000000000012, 3.0)
print(math.modf(3.14))
print(math.pi)  # print the value of pi which is 3.141592653589793
print(math.pow(2, 3))  # print the value of 2 raised to the power of 3 which is 8
print(math.sin(math.pi/2))  # print the sine of pi/2 which is 1.0
print(math.sinh(0))  # print the hyperbolic sine of 0 which is 0.0
print(math.sqrt(16))  # print the square root of 16 which is 4.0
print(math.tan(math.pi/4))  # print the tangent of pi/4 which is 1.0
print(math.tanh(0))  # print the hyperbolic tangent of 0 which is 0.0
